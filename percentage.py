x=int(input('Enter The Mark '))

if (x<60):
    print('Student Failed')
elif (x>=60 and x<=69):
    print('Student Scored D+ Grade')
elif (x>=70 and x<=79):
    print('Student Scored C+ Grade')
elif (x>=80 and x<=89):
    print('Student Scored B+ Grade')
elif (x>=90 and x<=100):
    print('Student Scored A+ Grade')



